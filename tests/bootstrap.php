    require dirname(__DIR__).'/config/bootstrap.php';
} elseif (method_exists(Dotenv::class, 'bootEnv')) {
    $fileName = dirname(__DIR__).'/.env.test';
    if (!file_exists($fileName)) {
        $fileName = dirname(__DIR__).'/.env';
    }
    (new Dotenv())->bootEnv($fileName);
}
